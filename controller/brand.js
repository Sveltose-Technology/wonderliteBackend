const Brand = require('../models/brand')


exports.addbrand = async (req, res) => {
    const { name, desc, sortorder, status } = req.body

    const newBrand = new Brand({
        name: name,
        desc: desc,
        sortorder: sortorder,
        status: status
    });

    const findexist = await Brand.findOne({ name: name })
    if (findexist) {
        res.status(400).json({
            status: false,
            msg: "Already Exists",
            data: {}
        })
    } else {
        newBrand.save()
            .then(
                res.status(200).json({
                    status: true,
                    msg: "success",
                    data: newBrand
                })
            )
            .catch(error => {
                res.status(400).json({
                    status: false,
                    msg: "error",
                    error: error
                })
            })
    }
}


exports.editbrand = async (req, res) => {
    const findandUpdateEntry = await Brand.findOneAndUpdate({
        _id: req.params.id
    }, { $set: req.body }, { new: true })
    if (findandUpdateEntry) {
        res.status(200).json({
            status: true,
            msg: "success",
            data: findandUpdateEntry
        })
    } else {
        res.status(400).json({
            status: false,
            msg: "error",
            error: "error"
        })
    }
}


